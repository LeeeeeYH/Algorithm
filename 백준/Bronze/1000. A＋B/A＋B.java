import java.util.Scanner;

/**
 * 백준 문제의 기본 class 이름은 Main이므로 main함수를 단축키로 사용시 두 번째 것을 사용한다.
 */
public class Main {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int A = sc.nextInt();
		int B = sc.nextInt();
		System.out.println(A+B);
		
		
	} // end of main
} // end of class
